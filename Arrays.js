let myArray = [1, 2, 3, 4, 5, 6, 7, 8, 9];
console.log(myArray);

// Some Methods of Arrays
myArray.push(10); // it pushes new  value at last of Array
console.log(myArray);

myArray.pop(); // it takes out the last value from Array
console.log(myArray);

myArray.unshift(9); // it adds one value at the start of Array
console.log(myArray);

myArray.shift(); // it removes the first value of an Array
console.log(myArray);

let newArray = myArray.join(); // .join just Adds all elements of an Array into String
console.log(myArray);
console.log(newArray);










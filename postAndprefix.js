// ++++++++++ Prefix and Postfix In JS +++++++++++


// let x = 2; y = ++x; Prefix:- First value of x is increased,updated then assigned to y means x=3 y=3
// let x = 2; y = x++; Postfix:- First value of x is assigned to y then increased,updated means x=3 y=2


// Example of Prefix
let x = 100;
let y = ++x;

console.log(x);
console.log(y);

// Example of Postfix
let a = 100;
let b = a++;

console.log(a);
console.log(b);



